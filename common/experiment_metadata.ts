/** Matches backend search-relevance experiment name/description limits. */
export const EXPERIMENT_NAME_MAX_LENGTH = 50;
export const EXPERIMENT_DESCRIPTION_MAX_LENGTH = 250;

/** Shown when an experiment has no user-visible name (legacy docs or empty string). */
export const UNNAMED_EXPERIMENT_LABEL = 'Unnamed experiment';

export type ExperimentMetadataFieldErrors = {
  name?: string[];
  description?: string[];
};

/**
 * Returns a non-empty display name for list/detail views.
 */
export function getExperimentDisplayName(name: string | undefined | null): string {
  const trimmed = typeof name === 'string' ? name.trim() : '';
  if (trimmed.length > 0) {
    return trimmed;
  }
  return UNNAMED_EXPERIMENT_LABEL;
}

/**
 * Detects backend default names like `PAIRWISE_COMPARISON-a1b2c3d4` so the UI can nudge users to customize.
 */
export function isLikelyAutoGeneratedExperimentName(
  name: string | undefined | null,
  experimentType: string
): boolean {
  const trimmed = typeof name === 'string' ? name.trim() : '';
  if (!trimmed) {
    return false;
  }
  return new RegExp(`^${experimentType}-[a-f0-9]{8}$`, 'i').test(trimmed);
}

/**
 * Client-side validation for optional experiment name/description on create/edit.
 */
export function validateExperimentMetadataForCreate(
  name: string | undefined,
  description: string | undefined
): ExperimentMetadataFieldErrors {
  const errors: ExperimentMetadataFieldErrors = {};
  const n = typeof name === 'string' ? name : '';
  const d = typeof description === 'string' ? description : '';

  if (n.length > EXPERIMENT_NAME_MAX_LENGTH) {
    errors.name = [`Name must be at most ${EXPERIMENT_NAME_MAX_LENGTH} characters.`];
  }
  if (d.length > EXPERIMENT_DESCRIPTION_MAX_LENGTH) {
    errors.description = [
      `Description must be at most ${EXPERIMENT_DESCRIPTION_MAX_LENGTH} characters.`,
    ];
  }
  return errors;
}
