import {
  EXPERIMENT_DESCRIPTION_MAX_LENGTH,
  EXPERIMENT_NAME_MAX_LENGTH,
  UNNAMED_EXPERIMENT_LABEL,
  getExperimentDisplayName,
  isLikelyAutoGeneratedExperimentName,
  validateExperimentMetadataForCreate,
} from './experiment_metadata';

describe('getExperimentDisplayName', () => {
  it('returns trimmed name when provided', () => {
    expect(getExperimentDisplayName('  My experiment  ')).toBe('My experiment');
  });

  it('returns unnamed label for empty, whitespace, or missing values', () => {
    expect(getExperimentDisplayName('')).toBe(UNNAMED_EXPERIMENT_LABEL);
    expect(getExperimentDisplayName('   ')).toBe(UNNAMED_EXPERIMENT_LABEL);
    expect(getExperimentDisplayName(undefined)).toBe(UNNAMED_EXPERIMENT_LABEL);
    expect(getExperimentDisplayName(null)).toBe(UNNAMED_EXPERIMENT_LABEL);
  });
});

describe('isLikelyAutoGeneratedExperimentName', () => {
  it('detects default backend naming patterns', () => {
    expect(
      isLikelyAutoGeneratedExperimentName('PAIRWISE_COMPARISON-a1b2c3d4', 'PAIRWISE_COMPARISON')
    ).toBe(true);
    expect(
      isLikelyAutoGeneratedExperimentName('PAIRWISE_COMPARISON-A1B2C3D4', 'PAIRWISE_COMPARISON')
    ).toBe(true);
  });

  it('does not flag customized names', () => {
    expect(isLikelyAutoGeneratedExperimentName('My custom experiment', 'PAIRWISE_COMPARISON')).toBe(
      false
    );
  });
});

describe('validateExperimentMetadataForCreate', () => {
  it('allows empty optional fields', () => {
    expect(validateExperimentMetadataForCreate('', '')).toEqual({});
    expect(validateExperimentMetadataForCreate(undefined, undefined)).toEqual({});
  });

  it('rejects names longer than the maximum length', () => {
    const name = 'a'.repeat(EXPERIMENT_NAME_MAX_LENGTH + 1);
    expect(validateExperimentMetadataForCreate(name, '')).toEqual({
      name: [`Name must be at most ${EXPERIMENT_NAME_MAX_LENGTH} characters.`],
    });
  });

  it('rejects descriptions longer than the maximum length', () => {
    const description = 'b'.repeat(EXPERIMENT_DESCRIPTION_MAX_LENGTH + 1);
    expect(validateExperimentMetadataForCreate('', description)).toEqual({
      description: [
        `Description must be at most ${EXPERIMENT_DESCRIPTION_MAX_LENGTH} characters.`,
      ],
    });
  });
});
