import {
  EuiBadge,
  EuiButtonEmpty,
  EuiFlexGroup,
  EuiFlexItem,
  EuiPageHeader,
  EuiPageTemplate,
  EuiSpacer,
  EuiText,
  EuiToolTip,
} from '@elastic/eui';
import React, { useEffect, useState } from 'react';
import { RouteComponentProps, withRouter } from 'react-router-dom';
import { CoreStart } from '../../../../../../src/core/public';
import {
  getExperimentDisplayName,
  isLikelyAutoGeneratedExperimentName,
} from '../../../../common';
import { ExperimentType, toExperiment } from '../../../types/index';
import { ExperimentService } from '../services/experiment_service';
import { ExperimentEditMetadataModal } from './experiment_edit_metadata_modal';
import { PairwiseExperimentViewWithRouter } from './pairwise_experiment_view';
import { EvaluationExperimentViewWithRouter } from './evaluation_experiment_view';
import { HybridOptimizerExperimentViewWithRouter } from './hybrid_optimizer_experiment_view';

interface ExperimentViewProps extends RouteComponentProps<{ id: string }> {
  http: CoreStart['http'];
  notifications: CoreStart['notifications'];
  dataSourceId?: string | null;
}

export const ExperimentView: React.FC<ExperimentViewProps> = ({
  http,
  notifications,
  id,
  dataSourceId,
  history,
}) => {
  const [experiment, setExperiment] = useState<any | null>(null);
  const [refreshRevision, setRefreshRevision] = useState(0);
  const [isEditOpen, setIsEditOpen] = useState(false);
  const experimentService = new ExperimentService(http);

  useEffect(() => {
    const fetchExperiment = async () => {
      try {
        const response = await experimentService.getExperiment(id, dataSourceId);
        const source = response?.hits?.hits?.[0]?._source;
        if (source) {
          const parsedExperiment = toExperiment(source);
          if (parsedExperiment.success) {
            setExperiment(parsedExperiment.data);
          } else {
            console.error('Invalid experiment data format');
            setExperiment(null);
          }
        } else {
          console.error('No matching experiment found');
          setExperiment(null);
        }
      } catch (err) {
        console.error('Failed to fetch experiment', err);
        console.error(err);
        setExperiment(null);
      }
    };

    fetchExperiment();
  }, [http, id, dataSourceId, refreshRevision]);

  const pageTitle = experiment ? getExperimentDisplayName(experiment.name) : 'Experiment details';
  const descriptionText =
    experiment &&
    typeof experiment.description === 'string' &&
    experiment.description.trim() !== ''
      ? experiment.description.trim()
      : undefined;

  const isProcessing = experiment?.status === 'PROCESSING';

  const editButton = (
    <EuiButtonEmpty
      iconType="pencil"
      disabled={isProcessing}
      onClick={() => setIsEditOpen(true)}
      data-test-subj="experimentEditMetadata"
    >
      Edit
    </EuiButtonEmpty>
  );

  return (
    <EuiPageTemplate paddingSize="l" restrictWidth="90%">
      <EuiPageHeader
        pageTitle={
          experiment ? (
            <EuiFlexGroup gutterSize="s" alignItems="center" responsive={false} wrap>
              <EuiFlexItem grow={false}>
                <span>{pageTitle}</span>
              </EuiFlexItem>
              {isLikelyAutoGeneratedExperimentName(experiment.name, experiment.type) && (
                <EuiFlexItem grow={false}>
                  <EuiToolTip content="This name was auto-generated. Click Edit to customize.">
                    <EuiBadge color="hollow">Auto</EuiBadge>
                  </EuiToolTip>
                </EuiFlexItem>
              )}
              <EuiFlexItem grow={false}>
                {isProcessing ? (
                  <EuiToolTip content="You cannot edit details while the experiment is processing.">
                    <span>{editButton}</span>
                  </EuiToolTip>
                ) : (
                  editButton
                )}
              </EuiFlexItem>
            </EuiFlexGroup>
          ) : (
            pageTitle
          )
        }
        description={descriptionText}
      />
      {experiment && (
        <>
          <EuiSpacer size="s" />
          <EuiText size="s" color="subdued">
            Experiment ID: {experiment.id}
          </EuiText>
        </>
      )}
      {experiment && experiment.type === ExperimentType.PAIRWISE_COMPARISON && (
        <PairwiseExperimentViewWithRouter
          http={http}
          notifications={notifications}
          inputExperiment={experiment}
          history={history}
          dataSourceId={dataSourceId}
        />
      )}
      {experiment && experiment.type === ExperimentType.POINTWISE_EVALUATION && (
        <EvaluationExperimentViewWithRouter
          http={http}
          notifications={notifications}
          inputExperiment={experiment}
          history={history}
          dataSourceId={dataSourceId}
        />
      )}
      {experiment && experiment.type === ExperimentType.HYBRID_OPTIMIZER && (
        <HybridOptimizerExperimentViewWithRouter
          http={http}
          notifications={notifications}
          inputExperiment={experiment}
          history={history}
          dataSourceId={dataSourceId}
        />
      )}
      {experiment && (
        <ExperimentEditMetadataModal
          isOpen={isEditOpen}
          onClose={() => setIsEditOpen(false)}
          experimentId={experiment.id}
          initialName={experiment.name}
          initialDescription={experiment.description}
          dataSourceId={dataSourceId}
          experimentService={experimentService}
          onSaved={() => {
            notifications.toasts.addSuccess({
              title: 'Experiment details updated.',
            });
            setRefreshRevision((r) => r + 1);
          }}
        />
      )}
    </EuiPageTemplate>
  );
};

export const ExperimentViewWithRouter = withRouter(ExperimentView);

export default ExperimentViewWithRouter;
